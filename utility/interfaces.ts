
/**
 * Represents a RGBA color.
 */
export interface Color {
    /**
     * Color's transparency value (alpha channel).
     */
    alpha: number
    /**
     * Red color value.
     */
    red: number
    /**
     * Green color value.
     */
    green: number
    /**
     * Blue color value.
     */
    blue: number
}
